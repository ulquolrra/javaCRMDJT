package DayTwo;

public class Arith {
	public static void main(String[] args){
		float number1 = 45.56f;
		int number2 = 152;
		System.out.println("sum is " + (number1 + number2));
		System.out.println("- is " + (number2 - number1));
		System.out.println("* is " + number2*number1);
		System.out.println("/ is " + number1/number2);
	}
}
